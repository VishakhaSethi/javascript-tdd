let addition = function additionValues(a,b) {
  return a + b;
};

let subtraction = function subtractionValues(a,b) {
  return a - b;
};

let multiply = function multiplyValues(a,b) {
  return a * b;
};

let divide = function divideValues(a,b) {
  return a / b;
};


export {
  addition,
  subtraction,
  multiply,
  divide
};
