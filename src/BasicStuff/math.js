// Simple math function

const addition = (a,b) => {
  return a + b;
};

const subtraction = (a,b) => {
  return a - b;
};

const multiply = (a,b) => {
  return a * b;
};

const divide = (a,b) => {
  return a / b;
};

const squareRoot = (a) => {
  return Math.sqrt(a);
};

export {
  addition,
  subtraction,
  multiply,
  divide,
  squareRoot
};
